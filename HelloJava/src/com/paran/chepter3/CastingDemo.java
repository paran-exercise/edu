package com.paran.chepter3;


public class CastingDemo {
	int num1;
	long num2;
	float num3;
		
		public static void main(String[] args){
			
			CastingDemo js = new CastingDemo();
		
			js.num1 = 10000000000;
			js.num2 = 10000000000;
			js.num3 = 12.45;
			
			
			
			System.out.println(js.num1);
			System.out.println(js.num2);
			System.out.println(js.num3);
			  
			
		}
}
